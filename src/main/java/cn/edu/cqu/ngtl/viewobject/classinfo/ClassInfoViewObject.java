public class ClassInfoViewObject {

    // 课程名称//
    private String className;

    // 课程编号//
    private String classId;

    // 课程类别//
    private String classKind;

    // 学时//
    private String classHour;

    // 学分//
    private String credit;

    // 上课教师//
    private String instructor;

    // 所属学院//
    private String courseDepartment;

    // 必修/选修
    private String required;

    // 成绩评定方式
    private String grade;

    // 教学班号
    private String classNumber;

    // 上课教师人数
    private String instructorNumber;

    // 选课人数//
    private String studentNumber;

    // 助教人数
    private String taNumber;

    // 工时
    private String workHour;

    // 平时成绩占比
    private String gradePercent;

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassId() {
        return classId;
    }

    public void setClassId(String classId) {
        this.classId = classId;
    }

    public String getClassKind() {
        return classKind;
    }

    public void setClassKind(String classKind) {
        this.classKind = classKind;
    }

    public String getClassHour() {
        return classHour;
    }

    public void setClassHour(String classHour) {
        this.classHour = classHour;
    }

    public String getCredit() {
        return credit;
    }

    public void setCredit(String credit) {
        this.credit = credit;
    }

    public String getInstructor() {
        return instructor;
    }

    public void setInstructor(String instructor) {
        this.instructor = instructor;
    }

    public String getCourseDepartment() {
        return courseDepartment;
    }

    public void setCourseDepartment(String courseDepartment) {
        this.courseDepartment = courseDepartment;
    }

    public String getRequired() {
        return required;
    }

    public void setRequired(String required) {
        this.required = required;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getClassNumber() {
        return classNumber;
    }

    public void setClassNumber(String classNumber) {
        this.classNumber = classNumber;
    }

    public String getInstructorNumber() {
        return instructorNumber;
    }

    public void setInstructorNumber(String instructorNumber) {
        this.instructorNumber = instructorNumber;
    }

    public String getStudentNumber() {
        return studentNumber;
    }

    public void setStudentNumber(String studentNumber) {
        this.studentNumber = studentNumber;
    }

    public String getTaNumber() {
        return taNumber;
    }

    public void setTaNumber(String taNumber) {
        this.taNumber = taNumber;
    }

    public String getWorkHour() {
        return workHour;
    }

    public void setWorkHour(String workHour) {
        this.workHour = workHour;
    }

    public String getGradePercent() {
        return gradePercent;
    }

    public void setGradePercent(String gradePercent) {
        this.gradePercent = gradePercent;
    }
}